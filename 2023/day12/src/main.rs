#[derive(Debug, PartialEq, Eq, Clone)]
enum Marker {
    Unknown,
    Fixed,
    Broken,
    None,
}

impl Marker {
    fn new(c: char) -> Marker {
        match c {
            '?' => Marker::Unknown,
            '#' => Marker::Broken,
            '.' => Marker::Fixed,
            _ => panic!("Unknown character"),
        }
    }
}

#[derive(Debug, Clone)]
struct Section {
    marker: Marker,
    len: usize, 
}


#[derive(Debug)]
struct Field {
    markers: Vec<Marker>,
    secs: Vec<Section>,
    marker_string: String,
}


fn get_counts(n: usize, counts: &Vec<usize>) -> Option<usize> {
    let min_len: usize = counts.iter().sum::<usize>() + counts.iter().count() - 1;
    if min_len > n {
        return Some(0);
    }
    if min_len == n {
        return Some(1);
    }
    let extra_len = n - min_len;
    if counts.len() == 1 {
        let tmp = n - counts.last().unwrap() + 1;
        return Some(tmp);
    }
    // Works when extra len is 1
    match (extra_len, counts.len())  {
        (1,_) => Some(extra_len * (1 + counts.len())),
        // 2 groups, this is solved
        (n,2) => Some((n+1) * (n + 2) / 2),
        // Triangular numbers with N = extra_len + 1 
        // Typical definition is n * n(+1) / 2

        // 3 groups, 
        //(n,3) => {
        //    Some(binomial(n+1 + 3 -1,3))
        //    //Some(((n+1) * (n+2) * (n+3) /6))
        //    // Tetrahedral numbers with N = extra_len + 1 
        //    // Typical definition is n * n(+1) * (n+2) / 6
        //},
        //// 4 groups, 
        //(n,4) => {
        //    Some(binomial(n +1 + 4 -1,4))
        //},
        (n,k) => {
            Some(binomial(n +k, k))
        }

        //
        //
        (n, c) => panic!("{n} {c}"),
        //(n,3) => 12, // 
        //(2,4) => 10, // Not correct!
        
        //(3,3) => 10, // Not correct!
        //(3,4) => 10, // Not correct!
        
        //(4,3) => 10, // Not correct!
        //(4,4) => 10, // Not correct!
        //(4,5) => 10, // Not correct!
        
        //(5,3) => 10, // Not correct!
        //_ => panic!("Unknown combo extra N: {}, count: {}", extra_len, counts.len()), 
    }
}

impl Field {
    fn new(v: &str, skim: bool) -> Field{
        let mut vv: &str = &v.replacen("..",".",10).replacen("..",".",10);
        if skim {
            vv = vv.trim_matches('.');
        }
        let mut f = Field {marker_string: vv.to_string(), secs: Vec::new(), markers: vv.chars().map(|c| {Marker::new(c)}).collect()};
        let mut prev = Marker::None;
        let mut sec_len: usize = 0;
        for m in &f.markers {
            if (m == &prev) | (prev == Marker::None) {
                sec_len += 1;
            }
            else {
                f.secs.push(Section {marker: prev, len: sec_len});
                sec_len = 1;
            }
            prev = m.clone();
        }
        f.secs.push(Section {marker: prev, len: sec_len});
        f
    }

    fn possible(&self) -> usize {
        self.secs.iter().map(|m| {
            match m.marker {
                Marker::Unknown | Marker::Broken => Some(m.len),
                Marker::Fixed => None,
                Marker::None => panic!("Panic"),
            }
        }).while_some().sum()
    }

    fn unknown(&self) -> usize {
        // Return amount of unknows at start
        self.secs.iter().map(|m| {
            match m.marker {
                Marker::Unknown => Some(m.len),
                Marker::Fixed | Marker::Broken => None,
                Marker::None => panic!("Panic"),
            }
        }).while_some().sum()
    }

    fn get_sections(&self) -> Vec<usize> {
        self.secs.iter().filter_map(|m| {
            match m.marker {
                Marker::Broken => Some(m.len),
                _ => None,
            }
        }).collect()
    }

    fn get_options(&self) -> Vec<Vec<usize>> {
        // Brute force method
        get_options(&self.marker_string)
    }

    fn len(&self) -> usize {
        self.markers.len()
    }

    fn get_options_alt(&self, counts: &Vec<usize>) -> usize {
        if counts.is_empty() {
            if self.markers.iter().filter(|m| m == &&Marker::Broken).count() == 0 {
                return 1;
            }
            return 0;
        }
        if (self.secs.len() == 1) {
            let sec = self.secs.last().unwrap();
            if sec.marker == Marker::Unknown {
                if let Some(v) = get_counts(sec.len, counts) {
                    return v
                }
            }
        }
        let min_len = counts.iter().sum::<usize>() + counts.len() - 1;
        if self.len() < min_len {
            return 0;
        }
        let c1 = *counts.iter().next().unwrap();
        let c_last = *counts.iter().last().unwrap();
        let n = self.len();
        if self.secs.len() > 1 {
            let last_sec = self.secs.iter().last().unwrap();
            match last_sec.marker {
                Marker::Broken => {
                    let n = self.len();
                    if n < c_last {
                        return 0;
                    }
                    if n > c_last {
                        let sub_field = Field::new(self.marker_string.get(0..n-c_last-1).unwrap(), true);
                        let sub_counts = counts[0..counts.len()-1].to_vec();
                        return sub_field.get_options_alt(&sub_counts);
                    }
                    return 1;
                },
                Marker::Fixed =>{
                    let l = last_sec.len;
                    let sub_field = Field::new(self.marker_string.get(0..n-l).unwrap(), true);
                    return sub_field.get_options_alt(counts);
                }
                _ => (),
            }
        }
        let first_sec = self.secs.first().unwrap();
        match first_sec.marker {
            Marker::Broken => {
                let n = self.len();
                if (n < c1) | (c1 > self.possible()) {
                    0
                } else if n > c1 {
                    if let Some(Marker::Broken) = self.markers.get(c1) { 0 }
                    else {
                        let sub_field = Field::new(self.marker_string.get(c1+1..n).unwrap(), true);
                        let sub_counts = counts[1..counts.len()].to_vec();
                        sub_field.get_options_alt(&sub_counts)
                    }
                } else { 1 }
            }
            Marker::Fixed =>{
                let l = first_sec.len;
                let sub_field = Field::new(self.marker_string.get(l..n).unwrap(), true);
                sub_field.get_options_alt(counts)
            }
            Marker::Unknown => {
                let f1 = Field::new(&self.marker_string.replacen('?', "#", 1), true);
                let f2 = Field::new(&self.marker_string.replacen('?', ".", 1), true);
                f1.get_options_alt(counts) + f2.get_options_alt(counts)
            },
            Marker::None => {panic!("HEY");},
        }
    }
}

fn get_options(markers: &str) -> Vec<Vec<usize>> {
    // Brute force solution
    let n: usize = markers.chars().map(|c| {
        match c {
            '?' => 1,
            _ => 0,
        }
    }).sum();

    if n == 0 {
        let f = Field::new(markers, true);
        vec![f.get_sections()]
    } else {
        let marker1 = markers.replacen('?', "#", 1);
        let marker2 = markers.replacen('?', ".", 1);
        let mut vec1 = get_options(&marker1);
        let mut vec2 = get_options(&marker2);
        vec1.append(&mut vec2);
        vec1
    }
}

