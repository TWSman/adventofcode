#[derive(Debug)]
enum RetType {
    Type1(Field),
    Type2(Vec<usize>),
}

fn read_line(input: &str, repeat: usize) -> usize {
    if input.parse::<usize>().is_ok()  {
        return input.parse::<usize>().unwrap();
    }

    let (field, mut counts) = match input.split_whitespace().with_position().map(|(p,v)| {
        match p {
            Position::First => {
                RetType::Type1(Field::new(&(0..repeat).map(|_| v).join("?"), true))
            },
            Position::Last => {
                RetType::Type2(v.split(',').map(|m| {m.parse::<usize>().unwrap()}).collect())
            }
            _ => panic!("Unknown position"),
        }
    }).collect_tuple().unwrap() {
            (RetType::Type1(x), RetType::Type2(y)) => (x,y),
            _ => panic!("Unknown type"),
        };

    let c = counts.clone();
    for _ in 0..(repeat-1) {
        counts.append(&mut c.clone());
    }
    // let options = field.get_options();
    // options.iter().filter(|m| { m == &&counts}).count()
    field.get_options_alt(&counts)
}

fn read_contents_conc(cont: &str, repeat: usize) -> usize {
    cont.lines().enumerate().collect::<Vec<_>>().par_iter().map(|(_i, l)| {
        println!("{}", &l);
        let tmp = read_line(l, repeat);
        println!("{} got {}", &l, tmp);
        tmp
    }).sum()
}

fn read_contents(cont: &str, repeat: usize) -> usize {
    cont.lines().enumerate().map(|(_, l)| {
        read_line(l, repeat)}
    ).sum()
}

